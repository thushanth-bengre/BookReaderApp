    public void populatetable() {
        DefaultTableModel model = (DefaultTableModel) tblBooks.getModel();

        model.setRowCount(0);
        for (Book b:business.getBd().getListOfBooks()) {

            Object[] row = new Object[3];
            row[0] = b;
            row[1] = b.getAuthor().getName();
            row[2] = b.getCatagory().getName();
            model.addRow(row);
        }

    }
