    private void populateTable(){
        DefaultTableModel modelmes = (DefaultTableModel) tblMessages.getModel();        
        modelmes.setRowCount(0);
        for (WorkRequest wr: account.getWorkQueue().getWorkRequestList()) {
            if(wr instanceof LibraryRequest) {
                if(wr.getStatus() == "New"){
                    Object[] row = new Object[4];
                    row[0] = wr;
                    row[1] = wr.getStatus();
                    row[2] = ((LibraryRequest) wr).getBook();
                    row[3] = ((LibraryRequest) wr).getLibName();
                    modelmes.addRow(row);                     
                    }
            }
        }
    }
    
    private Enterprise getEnterprise(UserAccount r){
        for(Network n: business.getNetworkList()){
                for(Enterprise e: n.getEnterpriseDirectory().getEnterpriseList()){
                    for(Organization o: e.getOrganizationDirectory().getOrganizationList()){                        
                            for(UserAccount ua: o.getUserAccountDirectory().getUserAccountList()){
                                if(ua.equals(r)){
                                    return e;                           
                            }
                        }
                    }
                }
            }
        return null;
    }
    
    private Organization getOrganization(UserAccount r){
        for(Network n: business.getNetworkList()){
                for(Enterprise e: n.getEnterpriseDirectory().getEnterpriseList()){
                    for(Organization o: e.getOrganizationDirectory().getOrganizationList()){                        
                            for(UserAccount ua: o.getUserAccountDirectory().getUserAccountList()){
                                if(ua.equals(r)){
                                    return o;                           
                            }
                        }
                    }
                }
            }
        return null;
    }

