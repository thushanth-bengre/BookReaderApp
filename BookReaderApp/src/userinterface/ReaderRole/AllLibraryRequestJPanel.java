    private void btnAcceptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAcceptActionPerformed
        // TODO add your handling code here:
        int selectedRowValue = tblMessages.getSelectedRow();
        if(selectedRowValue >= 0){
            LibraryRequest request = (LibraryRequest)tblMessages.getValueAt(selectedRowValue, 0);
            UserAccount r = (UserAccount) request.getSender();           
            request.setReason("Donated");
            request.setStatus("Accepted");
            Enterprise ent = getEnterprise(r);
            LibraryOrganization libOrg = (LibraryOrganization) getOrganization(r);
            Doners d = libOrg.getDl().addDoners();
            d.setBook(request.getBook());
            d.setName(account.getEmployee().getFirstName() + " " + account.getEmployee().getLastName());
            Message m = (Message) libOrg.getWorkQueue().createMessage();
            m.setSender(account);            
            m.setStatus("New");
            m.setMessageBody(accessibleContext + " has Donated book " + request.getBook().getName() + " to your library!!");            
            btnAccept.setEnabled(false);
            btnReject.setEnabled(false);
            populateTable();
        }
    }//GEN-LAST:event_btnAcceptActionPerformed

    private void btnRejectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRejectActionPerformed
        // TODO add your handling code here:
        int selectedRowValue = tblMessages.getSelectedRow();
        if(selectedRowValue >= 0){
            LibraryRequest request = (LibraryRequest)tblMessages.getValueAt(selectedRowValue, 0);
            UserAccount r = (UserAccount) request.getSender();
            LibraryOrganization libOrg = (LibraryOrganization) getOrganization(r);
            request.setStatus("Rejected");
            String reason = JOptionPane.showInputDialog(null,"Enter your reason for rejection","Reason",JOptionPane.QUESTION_MESSAGE);
            request.setReason(reason);
            btnAccept.setEnabled(false);
            btnReject.setEnabled(false);            
            Message m = (Message) libOrg.getWorkQueue().createMessage();
            m.setSender(account);            
            m.setStatus("New");
            m.setMessageBody(account + " has rejected your request to donate book " + request.getBook().getName() + " to your library and the reason is " + request.getReason());
            populateTable();
        }
    }//GEN-LAST:event_btnRejectActionPerformed


