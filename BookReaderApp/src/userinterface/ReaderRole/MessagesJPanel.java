    private void jButtonSendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSendActionPerformed
        // TODO add your handling code here:
        if(null != jTextAreaReplyContent.getText()){
        Readers r = (Readers) request.getSender();
        Message m = (Message) r.getWorkQueue().createMessage();
        m.setSender(account);
        m.setReceiver(r);
        m.setStatus("New");
        m.setMessageBody(jTextAreaReplyContent.getText());
        request.setStatus("Replied");
        JOptionPane.showMessageDialog(null,"Message Sent!!");
        }else{
            JOptionPane.showMessageDialog(null,"No Message written!!");
        }
    }//GEN-LAST:event_jButtonSendActionPerformed


