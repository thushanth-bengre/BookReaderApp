    public void populateTables(){
        DefaultTableModel modelnot = (DefaultTableModel) tblNotifications.getModel();
        DefaultTableModel modelmes = (DefaultTableModel) tblMessages.getModel();

        modelnot.setRowCount(0);
        modelmes.setRowCount(0);
        for (WorkRequest wr: account.getWorkQueue().getWorkRequestList()) {
            if("New".equals(wr.getStatus())){
            Object[] row = new Object[2];
            row[0] = wr;
            row[1] = wr.getStatus();
            if(wr instanceof Message)                
                modelmes.addRow(row);
            if(wr instanceof FollowRequest)
                modelnot.addRow(row);            
            }
        }
    }

