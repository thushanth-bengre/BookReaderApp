    private void btnAcceptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAcceptActionPerformed
        // TODO add your handling code here:
        int selectedRowValue = tblNotifications.getSelectedRow();
        if(selectedRowValue >= 0){
            FollowRequest request = (FollowRequest)tblNotifications.getValueAt(selectedRowValue, 0);
            Readers r = (Readers) request.getSender();
            r.addFollower(account);
            account.addFollower();
            request.setResponse("Accepted");
            request.setStatus("Responded");
            Message m = (Message) r.getWorkQueue().createMessage();
            m.setSender(account);
            m.setReceiver(r);
            m.setStatus("New");
            m.setMessageBody(account + " Accepted your Follow Request");
            btnFollowBack.setEnabled(true);
            btnAccept.setEnabled(false);            
            btnReject.setEnabled(false);
            JOptionPane.showMessageDialog(null,"Follow Request Accepted");
        }
    }//GEN-LAST:event_btnAcceptActionPerformed

