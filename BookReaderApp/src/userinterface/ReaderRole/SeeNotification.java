    private void btnRespondActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRespondActionPerformed
        // TODO add your handling code here:        
        int selectedRowValue = tblNotifications.getSelectedRow();
        if(selectedRowValue >= 0){
            btnAccept.setEnabled(true);        
            btnReject.setEnabled(true);
        }else{
            JOptionPane.showMessageDialog(null,"Please select a Request to Respond");
        }
    }//GEN-LAST:event_btnRespondActionPerformed

    private void btnRejectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRejectActionPerformed
        // TODO add your handling code here:
        int selectedRowValue = tblNotifications.getSelectedRow();
        if(selectedRowValue >= 0){
            FollowRequest request = (FollowRequest)tblNotifications.getValueAt(selectedRowValue, 0);
            request.setResponse("Rejected");
            request.setStatus("Responded");
            btnAccept.setEnabled(false);            
            btnReject.setEnabled(false);
            Readers r = (Readers) request.getSender();
            Message m = (Message) r.getWorkQueue().createMessage();
            m.setSender(account);
            m.setReceiver(r);
            m.setStatus("New");
            m.setMessageBody(account + " Rejected your Follow Request");
            populateTables();
        }
    }//GEN-LAST:event_btnRejectActionPerformed

    private void btnFollowBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFollowBackActionPerformed
        // TODO add your handling code here:
        int selectedRowValue = tblNotifications.getSelectedRow();
        if(selectedRowValue >= 0){
            FollowRequest request = (FollowRequest)tblNotifications.getValueAt(selectedRowValue, 0);
            Readers r = (Readers) request.getSender();
            FollowRequest fr = (FollowRequest) r.getWorkQueue().createFollowRequest();
            fr.setSender(account);
            fr.setReceiver(r);
            fr.setStatus("New");
            fr.setResponse("Pending");
            btnFollowBack.setEnabled(false);
            JOptionPane.showMessageDialog(null,"Follow Request Sent!!");
            populateTables();
            btnAccept.setEnabled(false);
            btnFollowBack.setEnabled(false);
            btnReject.setEnabled(false);
        }
    }//GEN-LAST:event_btnFollowBackActionPerformed

