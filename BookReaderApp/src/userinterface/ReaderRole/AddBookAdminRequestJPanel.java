    private void populateTable(){
        DefaultTableModel model = (DefaultTableModel) jTableRequest.getModel();
        model.setRowCount(0);
        for(WorkRequest wr: workQueue.getWorkRequestList()){
            if(wr instanceof AddBook){
                if(wr.getSender() == account){                    
                Object[] row = new Object[3];
                row[0] = ((AddBook) wr).getBook(); 
                row[1] = ((AddBook) wr).getAuthor();
                row[2] = wr.getStatus();                
                model.addRow(row);
                }
            }
        }
        
    }

