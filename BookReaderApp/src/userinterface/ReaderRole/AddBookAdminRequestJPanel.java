    private void jButtonSendRequestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSendRequestActionPerformed
        // TODO add your handling code here:
        String name = jTextFieldBookName.getText();
        String author = jTextFieldAuthorName.getText();
        if(name == null || null == author){
            JOptionPane.showMessageDialog(null,"Enter Book and Author name to send request");
        }else{
            AddBook ab = (AddBook) business.getWorkQueue().createAddBookRequest();
            ab.setStatus("New");
            ab.setSender(account);
            ab.setAuthor(author);
            ab.setBook(name);
            JOptionPane.showMessageDialog(null,"Request Sent to the admint to add this book");
            populateTable();
            jTextFieldAuthorName.setText("");
            jTextFieldBookName.setText("");
       }
    }//GEN-LAST:event_jButtonSendRequestActionPerformed


